# the memory buffer for the LCD
buffer = []

def SH1106_command(channel, data):
    spi_buffer = bytes(data)
    wiringpi.digitalWrite(PIN_DC, GPIO.LOW)
    wiringpi.wiringPiSPIDataRW(channel, spi_buffer)
    wiringpi.digitalWrite(PIN_DC, GPIO.HIGH)

def setDisplay():
    global buffer
    buffer = image_data

def clearDisplay():
    global buffer
    buffer = [0] * int(SH1106_LCDHEIGHT * SH1106_LCDWIDTH / 8)

def display(channel):
    global buffer
    command = [
        SH1106_SETLOWCOLUMN | 0x0,      # low col = 0
        SH1106_SETHIGHCOLUMN | 0x0,     # hi col = 0
        SH1106_SETSTARTLINE | 0x0       # line #0
    ]
    SH1106_command(channel, command)

    height = SH1106_LCDHEIGHT
    width  = SH1106_LCDWIDTH
    m_row = 0
    m_col = 2

    height >>= 3
    width >>= 3

    p = 0
    i = j = 0

    for i in range(0, height):
        # send a bunch of data in one xmission
        command = [
            0xB0 + i + m_row,       #set page address
            m_col & 0xf,            #set lower column address
            0x10 | (m_col >> 4)     #set higher column address
        ]
        SH1106_command(channel, command)
		
        for j in range(0, 8):
            spi_buffer = bytes(buffer[p:p+width])
            wiringpi.digitalWrite(PIN_DC, GPIO.HIGH)
            wiringpi.wiringPiSPIDataRW(channel, spi_buffer)
            p += width

def main():
    # initialize
    wiringpi.wiringPiSetup()
    wiringpi.pinMode(PIN_DC, GPIO.OUTPUT)        # DC
    wiringpi.pinMode(PIN_RES, GPIO.OUTPUT)       # RES
    wiringpi.digitalWrite(PIN_RES, GPIO.HIGH)
    wiringpi.digitalWrite(PIN_DC, GPIO.HIGH)

    spidev = wiringpi.wiringPiSPISetup(CHANNEL, SPEED)
    if(spidev < 0):
        print("Unable to open spi device.", file=sys.stderr)
        sys.exit(1)

    # Setup reset pin direction
    wiringpi.digitalWrite(PIN_RES, GPIO.HIGH)
    # VDD (3.3V) goes high at start, lets just chill for a ms
    time.sleep(0.001)
    # bring reset low
    wiringpi.digitalWrite(PIN_RES, GPIO.LOW)
    # wait 10ms
    time.sleep(0.01)
    # bring out of reset
    wiringpi.digitalWrite(PIN_RES, GPIO.HIGH)

    # Init sequence for 128x64 OLED module
    SH1106_command(CHANNEL, init_sequence)

    # main process
    while(True):
        setDisplay()
        display(CHANNEL)
        time.sleep(5)
        clearDisplay()
        display(CHANNEL)
        time.sleep(5)

    sys.exit(0)

if(__name__ == "__main__"):
    main()
