// the memory buffer for the LCD
static uint8 buffer[SH1106_LCDHEIGHT * SH1106_LCDWIDTH / 8];

static uint8 spi_buffer[32];
static uint8 command[4];

static void setDisplay(void);
static void clearDisplay(void);
static void display(int channel);
static void SH1106_command(int channel, uint8 *data, int size);

int main(void) {
    /* initialize */
    wiringPiSetup();
    pinMode(PIN_DC, OUTPUT);                // DC
    pinMode(PIN_RES, OUTPUT);               // RES
    digitalWrite(PIN_RES, HIGH);
    digitalWrite(PIN_DC, HIGH);

    int spidev = wiringPiSPISetup(CHANNEL, SPEED);
    if(spidev < 0) {
        fprintf(stderr, "Unable to open spi device.\n");
        return 1;
    }

    // Setup reset pin direction
    digitalWrite(PIN_RES, HIGH);
    // VDD (3.3V) goes high at start, lets just chill for a ms
    delay(1);
    // bring reset low
    digitalWrite(PIN_RES, LOW);
    // wait 10ms
    delay(10);
    // bring out of reset
    digitalWrite(PIN_RES, HIGH);

    /* Init sequence for 128x64 OLED module */
    SH1106_command(CHANNEL, (uint8 *)init_sequence, sizeof(init_sequence));

    /* main process */
    while(TRUE) {
        setDisplay();
        display(CHANNEL);
        delay(5000);
        clearDisplay();
        display(CHANNEL);
        delay(5000);
    }

    return 0;
}

static void setDisplay(void) {
  memcpy(buffer, image_data, sizeof(buffer));
}

static void clearDisplay(void) {
  memset(buffer, 0, sizeof(buffer));
}

static void display(int channel) {
    command[0] = SH1106_SETLOWCOLUMN | 0x0;     // low col = 0
    command[1] = SH1106_SETHIGHCOLUMN | 0x0;    // hi col = 0
    command[2] = SH1106_SETSTARTLINE | 0x0;     // line #0
    SH1106_command(channel, command, 3);

    uint8 height = SH1106_LCDHEIGHT;
    uint8 width  = SH1106_LCDWIDTH; 
    uint8 m_row = 0;
    uint8 m_col = 2;

    height >>= 3;
    width >>= 3;

    int p = 0;
    uint8 i, j = 0;

    for(i = 0; i < height; i++) {
        // send a bunch of data in one xmission
        command[0] = 0xB0 + i + m_row;      //set page address
        command[1] = m_col & 0xf;           //set lower column address
        command[2] = 0x10 | (m_col >> 4);   //set higher column address
        SH1106_command(channel, command, 3);
		
        for(j = 0; j < 8; j++) {
            digitalWrite(PIN_DC, HIGH);
            wiringPiSPIDataRW(channel, &buffer[p], width);
            p += width;
        }
	}
}

static void SH1106_command(int channel, uint8 *data, int size) {
    memcpy(spi_buffer, data, size);
    digitalWrite(PIN_DC, LOW);
    wiringPiSPIDataRW(channel, spi_buffer, size);
    digitalWrite(PIN_DC, HIGH);
}
